package za.cf.cp.club;

/**
 * Enum representing different types of clubs/organizations.
 * Used to differentiate between traditional clubs, venues, and other organization types.
 */
public enum ClubType {
    /**
     * Traditional padel club
     */
    CLUB,
    
    /**
     * Venue that can host tournaments and events
     */
    VENUE,
    
    /**
     * Padel academy or training facility
     */
    ACADEMY,
    
    /**
     * League or tournament organization
     */
    LEAGUE
}
